package com.znsd.bean;

public class RecordsBean {
	private Integer perId;
	private String userId;
	private Double score;
	private Integer paperId;
	private String testTime;
	private String paperName;
	public Integer getPerId() {
		return perId;
	}
	public void setPerId(Integer perId) {
		this.perId = perId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public Double getScore() {
		return score;
	}
	public void setScore(Double score) {
		this.score = score;
	}
	public Integer getPaperId() {
		return paperId;
	}
	public void setPaperId(Integer paperId) {
		this.paperId = paperId;
	}
	public String getTestTime() {
		return testTime;
	}
	public void setTestTime(String testTime) {
		this.testTime = testTime;
	}
	public String getPaperName() {
		return paperName;
	}
	public void setPaperName(String paperName) {
		this.paperName = paperName;
	}
	public RecordsBean(Integer perId, String userId, Double score, Integer paperId, String testTime, String paperName) {
		this.perId = perId;
		this.userId = userId;
		this.score = score;
		this.paperId = paperId;
		this.testTime = testTime;
		this.paperName = paperName;
	}
	public RecordsBean() {}
	@Override
	public String toString() {
		return "RecordsBean [perId=" + perId + ", userId=" + userId + ", score=" + score + ", paperId=" + paperId
				+ ", testTime=" + testTime + ", paperName=" + paperName + "]";
	}
	
}
