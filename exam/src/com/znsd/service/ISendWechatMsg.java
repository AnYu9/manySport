package com.znsd.service;

public interface ISendWechatMsg {
	
	/**
	 * 给父母发邮件   内容有格式 必须参考！！！！！
	 * 如下：
	 * 		SendWechatMsgImpl ss = new SendWechatMsgImpl();
	 * 		ss.sendMsg("测试2", "第一名**********99************货哦、第二名**********100*********胡货哦, "呵呵");
	 * 		
	 * 		一个数字一个*号         汉字两个
	 * 
	 * @param title 	标题
	 * @param content	内容
	 * @param remark	内容的补充
	 * @return
	 * @throws Exception
	 */
	public abstract String sendMsg(String title,String content,String remark) throws Exception ;

}
